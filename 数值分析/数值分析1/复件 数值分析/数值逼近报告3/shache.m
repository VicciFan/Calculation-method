function [z]=shache(x,y,k,t)
m=length(x);
n=length(k);
A(m,n)=0;
for i=1:m,for j=1:n,
A(i,j)=(x(i))^k(j);
    end;end;
s=(A'*A)\(A'*y');
l=length(t);
q=zeros(1,l+1);
t(l+1)=50;
for i=1:n
    q=q+s(i)*t.^k(i);
end
z=q(1:l);
q(l+1)


